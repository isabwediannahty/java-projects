public class forloop2 {

    public static void main(String args[])
    {
        for (int number = 1; number <= 5; number++)
        {
            System.out.println("Number: " + number);
        }

        for (int number = 5; number >= 0; number--)
        {
            System.out.println("Number: " + number);
        }
    }
}